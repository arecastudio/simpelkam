package spk.model;

public class DataKampung {
    private String id, kode_desa_pum, desa_kelurahan, kecamatan, kabupaten_kota, provinsi, tahun_bentuk;
    private String dasar_hukum,peta_resmi_wilayah, lat, lon, utara, selatan, timur, barat;

    public DataKampung() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKode_desa_pum() {
        return kode_desa_pum;
    }

    public void setKode_desa_pum(String kode_desa_pum) {
        this.kode_desa_pum = kode_desa_pum;
    }

    public String getDesa_kelurahan() {
        return desa_kelurahan;
    }

    public void setDesa_kelurahan(String desa_kelurahan) {
        this.desa_kelurahan = desa_kelurahan;
    }

    public String getKecamatan() {
        return kecamatan;
    }

    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    public String getKabupaten_kota() {
        return kabupaten_kota;
    }

    public void setKabupaten_kota(String kabupaten_kota) {
        this.kabupaten_kota = kabupaten_kota;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getTahun_bentuk() {
        return tahun_bentuk;
    }

    public void setTahun_bentuk(String tahun_bentuk) {
        this.tahun_bentuk = tahun_bentuk;
    }

    public String getDasar_hukum() {
        return dasar_hukum;
    }

    public void setDasar_hukum(String dasar_hukum) {
        this.dasar_hukum = dasar_hukum;
    }

    public String getPeta_resmi_wilayah() {
        return peta_resmi_wilayah;
    }

    public void setPeta_resmi_wilayah(String peta_resmi_wilayah) {
        this.peta_resmi_wilayah = peta_resmi_wilayah;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLon() {
        return lon;
    }

    public void setLon(String lon) {
        this.lon = lon;
    }

    public String getUtara() {
        return utara;
    }

    public void setUtara(String utara) {
        this.utara = utara;
    }

    public String getSelatan() {
        return selatan;
    }

    public void setSelatan(String selatan) {
        this.selatan = selatan;
    }

    public String getTimur() {
        return timur;
    }

    public void setTimur(String timur) {
        this.timur = timur;
    }

    public String getBarat() {
        return barat;
    }

    public void setBarat(String barat) {
        this.barat = barat;
    }
}
